# Произведение цифр числа и среднее арифметическое
num = int(input("Введите 5-значное число: "))
mult = (num % 10) * ((num // 10) % 10) * ((num // 100) % 10) * ((num // 1000) % 10) * ((num // 10000) % 10)
print("Произведение цифр числа", num, "=", mult)
arith = ((num % 10) + ((num // 10) % 10) + ((num // 100) % 10) + ((num // 1000) % 10) + ((num // 10000) % 10)) / 5
print("Среднее арифметическое чиcла", num, "=", arith)

